// ==UserScript==
// @name         X Content Filter
// @version      1.17
// @updateURL    https://omba.nl/files/x-ai-filter/x-ai-filter-userscript.js
// @downloadURL  https://omba.nl/files/x-ai-filter/x-ai-filter-userscript.js
// @description  Analyzes and filters content on X.com based on configured topics
// @match        https://x.com/*
// @grant        GM.xmlHttpRequest
// @grant        GM.setValue
// @grant        GM.getValue
// ==/UserScript==

(function() {
    'use strict';

    const topicsConfig = [
        //{ topic: "politics", description: "posts about political subjects", threshold: 0.8 },
        { topic: "negativity", description: "posts with overly negative sentiment", threshold: 0.9 },
        { topic: "sexual_content", description: "posts containing pornographic or explicit sexual content", threshold: 0.75 },
        { topic: "china_propaganda", description: "posts containing propaganda related to China", threshold: 0.65 },
        { topic: "russian_language", description: "posts in Russian language", threshold: 1.0 },
        //{ topic: "noisy_content", description: "posts with noisy or disruptive content", threshold: 0.7 },
        { topic: "toxic_content", description: "posts with toxic or harmful content", threshold: 0.8 },
        //{ topic: "eyebait", description: "posts designed to attract attention without substance", threshold: 0.6 },
        //{ topic: "promoted_content", description: "posts promoted by X itself", threshold: 1.0 },
        //{ topic: "misinformation", description: "posts containing false or misleading information", threshold: 0.75 },
        { topic: "hate_speech", description: "posts containing hate speech or discriminatory content", threshold: 0.8 },
        { topic: "conspiracy_theories", description: "posts promoting unfounded conspiracy theories", threshold: 0.7 },
        { topic: "spam", description: "posts that are spam or unwanted commercial content", threshold: 0.85 },
        { topic: "violence", description: "posts containing violent content or inciting violence", threshold: 0.8 },
        { topic: "personal_attacks", description: "posts containing personal attacks or bullying", threshold: 0.75 },
        { topic: "extremism", description: "posts promoting extremist ideologies or radicalization", threshold: 0.8 },
        { topic: "pseudoscience", description: "posts promoting unscientific or debunked claims", threshold: 0.7 },
        //{ topic: "scams", description: "posts potentially containing scams or fraudulent content", threshold: 0.8 },
        //{ topic: "excessive_profanity", description: "posts with an unusually high amount of profanity", threshold: 0.85 },
        { topic: "drug_related", description: "posts promoting illegal drug use or trade", threshold: 0.75 },
        //{ topic: "copyright_infringement", description: "posts potentially infringing on copyrights", threshold: 0.7 },
        { topic: "doxxing", description: "posts revealing private or identifying information about individuals", threshold: 0.9 },
        { topic: "bot_activity", description: "posts likely generated by bots or automated accounts", threshold: 0.8 },
        // New topic added to filter low-quality content
        { topic: "low_quality_content", description: "posts that are low-quality, spammy, or have little informative value", threshold: 0.75 },
    ];

    let hiddenPostsCount = 0;
    let hiddenPostsLog = [];

    function createOverlay() {
        const overlay = document.createElement('div');
        overlay.id = 'hiddenPostsOverlay';
        overlay.style.cssText = 'position:fixed;top:0;right:0;z-index:9999;';
        document.body.appendChild(overlay);

        const bubble = document.createElement('div');
        bubble.style.cssText = 'font-family:sans-serif;background:#1DA1F2;color:#fff;border-radius:3px;padding:5px 8px;cursor:pointer;margin:5px;';
        bubble.onclick = toggleLog;
        overlay.appendChild(bubble);

        const log = document.createElement('div');
        log.style.cssText = 'font-family:sans-serif;display:none;background:#fff;border:1px solid #000;padding:10px;max-height:300px;overflow-y:auto;color:black;';
        overlay.appendChild(log);

        return { bubble, log };
    }

    const { bubble, log } = createOverlay();

    function toggleLog() {
        log.style.display = log.style.display === 'none' ? 'block' : 'none';
    }

    function updateOverlay(message) {
        hiddenPostsCount++;
        hiddenPostsLog.push(message);
        bubble.textContent = hiddenPostsCount;
        log.innerHTML = hiddenPostsLog.join('<br><br>');
    }

    async function checkForNewPosts() {
        const apiKey = await getGroqApiKey();
        if (!apiKey) {
            console.error("No API key provided. Aborting analysis.");
            return;
        }

        const posts = document.querySelectorAll('[data-testid="cellInnerDiv"]');

        posts.forEach(async post => {
            const tweetArticle = post.querySelector('article[data-testid="tweet"]');
            if (!tweetArticle) return;

            const postId = Array.from(tweetArticle.querySelectorAll('a'))
                .find(a => a.href.includes('/status/'))
                ?.href.split('/')
                .find((part, index, array) => array[index - 1] === 'status');
            const postTextElement = tweetArticle.querySelector('[data-testid="tweetText"]');
            const postText = postTextElement ? postTextElement.innerText.trim() : '';

            if (postId) {
                let analysis = await getCachedAnalysis(postId);
                if (!analysis) {
                    analysis = await analyzeTweet(postText, apiKey);
                    await cacheAnalysis(postId, analysis);
                }
                applyPostVisibility(postId, analysis, post);
            }
        });
    }

    async function getCachedAnalysis(postId) {
        return GM.getValue(`analysis_${postId}`, null);
    }

    async function cacheAnalysis(postId, analysis) {
        await GM.setValue(`analysis_${postId}`, analysis);
    }

    function applyPostVisibility(postId, analysis, postElement) {
        if (typeof analysis === 'object' && analysis !== null) {
            const shouldHide = topicsConfig.some(topic => 
                topic.topic in analysis && analysis[topic.topic] > topic.threshold
            );

            if (shouldHide) {
                if (!postElement.classList.contains('filtered-post')) {
                    postElement.classList.add('filtered-post');
                    const tweetUrl = `https://x.com/user/status/${postId}`;
                    const tweetText = postElement.querySelector('[data-testid="tweetText"]')?.innerText.trim() || 'Text not found';
                    const scores = topicsConfig.map(topic => `${topic.topic}: ${analysis[topic.topic].toFixed(2)}`).join(', ');
                    const message = `Post ${postId} hidden: ${tweetUrl}\n${tweetText}\nScores: ${scores}`;
                    updateOverlay(message);
                    createPlaceholder(postElement, postId);
                }
            } else {
                if (postElement.classList.contains('filtered-post')) {
                    postElement.classList.remove('filtered-post');
                    restoreOriginalContent(postElement, postId);
                }
            }
        }
    }

    function createPlaceholder(postElement, postId) {
        // Wrap the original content in a container if not already wrapped
        let originalContentWrapper = postElement.querySelector('.original-content-wrapper');
        if (!originalContentWrapper) {
            originalContentWrapper = document.createElement('div');
            originalContentWrapper.className = 'original-content-wrapper';
            // Move existing children into the wrapper
            while (postElement.firstChild) {
                originalContentWrapper.appendChild(postElement.firstChild);
            }
            postElement.appendChild(originalContentWrapper);
        }

        // Hide the original content
        originalContentWrapper.style.display = 'none';

        // Add the placeholder if it doesn't exist
        let placeholder = postElement.querySelector('.hidden-post-placeholder');
        if (!placeholder) {
            placeholder = document.createElement('div');
            placeholder.className = 'hidden-post-placeholder';
            placeholder.style.cssText = `
                background-color: #F7F9F9;
                border: 1px solid #E1E8ED;
                border-radius: 16px;
                padding: 20px;
                margin: 10px 0;
                color: #657786;
                font-size: 15px;
                line-height: 20px;
                text-align: center;
            `;
            placeholder.textContent = 'This post has been hidden based on your filter settings.';
            
            const revealButton = document.createElement('button');
            revealButton.textContent = 'Reveal Post';
            revealButton.style.cssText = `
                background-color: #1DA1F2;
                color: #FFFFFF;
                border: none;
                border-radius: 9999px;
                padding: 8px 16px;
                margin-top: 12px;
                cursor: pointer;
                font-size: 14px;
                font-weight: bold;
                font-family: 'Segoe UI', Arial, sans-serif;
            `;
            revealButton.onmouseover = () => {
                revealButton.style.backgroundColor = '#0d95e8';
            };
            revealButton.onmouseout = () => {
                revealButton.style.backgroundColor = '#1DA1F2';
            };
            revealButton.onclick = () => restoreOriginalContent(postElement, postId);
            
            // Center the button
            const buttonContainer = document.createElement('div');
            buttonContainer.style.cssText = 'display: flex; justify-content: center;';
            buttonContainer.appendChild(revealButton);
            placeholder.appendChild(buttonContainer);

            postElement.appendChild(placeholder);
        } else {
            placeholder.style.display = 'block';
        }
    }

    function restoreOriginalContent(postElement, postId) {
        // Show the original content
        const originalContentWrapper = postElement.querySelector('.original-content-wrapper');
        if (originalContentWrapper) {
            originalContentWrapper.style.display = '';
        }

        // Hide the placeholder
        const placeholder = postElement.querySelector('.hidden-post-placeholder');
        if (placeholder) {
            placeholder.style.display = 'none';
        }

        // Remove the post from the hidden posts log
        removeFromHiddenPostsLog(postId);
    }

    function removeFromHiddenPostsLog(postId) {
        hiddenPostsLog = hiddenPostsLog.filter(message => !message.includes(`Post ${postId} hidden:`));
        hiddenPostsCount = hiddenPostsLog.length;
        bubble.textContent = hiddenPostsCount;
        log.innerHTML = hiddenPostsLog.join('<br><br>');
    }

    async function analyzeTweet(tweetText, apiKey) {
        let retries = 0;
        const maxRetries = 3;
        const messages = [
            {
                role: "system",
                content: `Your task is to evaluate Tweets/X posts. Always respond in JSON. Follow this format:\n\n{\n${topicsConfig.map(topic => `    "${topic.topic}": 0.0`).join(',\n')}\n}\n\nRate the provided post from 0.0 to 1.0 for each topic. Here are the descriptions for each topic:\n\n${topicsConfig.map(topic => `${topic.topic}: ${topic.description}`).join('\n')}`
            },
            {
                role: "user",
                content: tweetText
            }
        ];

        while (retries < maxRetries) {
            try {
                const response = await new Promise((resolve, reject) => {
                    GM.xmlHttpRequest({
                        method: "POST",
                        url: "https://api.groq.com/openai/v1/chat/completions",
                        headers: {
                            "Content-Type": "application/json",
                            "Authorization": `Bearer ${apiKey}`
                        },
                        data: JSON.stringify({
                            messages: messages,
                            model: "deepseek-r1-distill-llama-70b-specdec",
                            temperature: 1,
                            max_tokens: 1024,
                            top_p: 1,
                            stream: false,
                            response_format: {
                                type: "json_object"
                            },
                            stop: null
                        }),
                        onload: resolve,
                        onerror: reject
                    });
                });

                if (response.status === 400) {
                    retries++;
                    continue;
                }

                const data = JSON.parse(response.responseText);
                return JSON.parse(data.choices[0].message.content);
            } catch (error) {
                retries++;
                if (retries === maxRetries) {
                    console.error("Max retries reached. Returning empty object.");
                    return {};
                }
            }
        }

        return {};
    }

    async function getGroqApiKey() {
        let apiKey = await GM.getValue('GROQ_API_KEY', null);
        if (!apiKey) {
            apiKey = prompt("Please enter your Groq API key:");
            if (apiKey) {
                await GM.setValue('GROQ_API_KEY', apiKey);
            }
        }
        return apiKey;
    }

    function debounce(func, delay) {
        let timeoutId;
        return function (...args) {
            clearTimeout(timeoutId);
            timeoutId = setTimeout(() => func.apply(this, args), delay);
        };
    }

    const debouncedCheck = debounce(checkForNewPosts, 300);

    window.addEventListener('scroll', () => {
        if (window.location.hostname === 'x.com') {
            debouncedCheck();
        }
    });

    if (window.location.hostname === 'x.com') {
        checkForNewPosts();
    }

})();
