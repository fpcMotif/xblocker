// Function to analyze a tweet using the Groq API
async function analyzeTweet(tweetText, apiKey) {
	let retries = 0;
	const maxRetries = 3;
	const messages = [
		{
			role: "system",
			content: `Your task is to evaluate Tweets/X posts. Always respond in JSON. Use the following format:\n\n{\n${topicsConfig
				.map((topic) => `    "${topic.topic}": 0.0`)
				.join(",\n")}\n}\n\nRate the provided post from 0.0 to 1.0 for each topic based on the descriptions:\n\n${topicsConfig
				.map((topic) => `${topic.topic}: ${topic.description}`)
				.join("\n")}`,
		},
		{
			role: "user",
			content: tweetText,
		},
	];

	while (retries < maxRetries) {
		try {
			const response = await fetch(
				"https://api.groq.com/openai/v1/chat/completions",
				{
					method: "POST",
					headers: {
						"Content-Type": "application/json",
						Authorization: `Bearer ${apiKey}`,
					},
					body: JSON.stringify({
						messages: messages,
						model: "llama-3.1-8b-instant",
						temperature: 1,
						max_tokens: 1024,
						top_p: 1,
						stream: false,
						response_format: {
							type: "json_object",
						},
						stop: null,
					}),
				},
			);

			if (!response.ok) {
				throw new Error(`HTTP error! status: ${response.status}`);
			}

			const data = await response.json();
			if (!data.choices || !data.choices[0] || !data.choices[0].message || !data.choices[0].message.content) {
				throw new Error("Invalid response structure from Groq API");
			}

			return JSON.parse(data.choices[0].message.content);
		} catch (error) {
			console.error(`Attempt ${retries + 1} failed:`, error);
			retries++;
			if (retries === maxRetries) {
				console.error("Max retries reached. Returning default analysis.");
				return topicsConfig.reduce((acc, topic) => {
					acc[topic.topic] = 0;
					return acc;
				}, {});
			}
			await new Promise(resolve => setTimeout(resolve, 1000 * retries));
		}
	}

	return {};
}

// Function to get or set the Groq API key
async function getGroqApiKey() {
	return new Promise((resolve) => {
		chrome.storage.local.get(["GROQ_API_KEY"], (result) => {
			if (result.GROQ_API_KEY) {
				resolve(result.GROQ_API_KEY);
			} else {
				const apiKey = prompt("Please enter your Groq API key:");
				if (apiKey) {
					chrome.storage.local.set({ GROQ_API_KEY: apiKey }, () => {
						resolve(apiKey);
					});
				} else {
					resolve(null);
				}
			}
		});
	});
}

// Debounce function to limit how often the scroll event fires
function debounce(func, delay) {
	let timeoutId;
	return function (...args) {
		clearTimeout(timeoutId);
		timeoutId = setTimeout(() => func.apply(this, args), delay);
	};
}

