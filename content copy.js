// Function to check for new posts on the page
async function checkForNewPosts() {
	// Early check for API key
	const apiKey = await getGroqApiKey();
	if (!apiKey) {
		console.error("No API key provided. Aborting analysis.");
		return;
	}

	const posts = document.querySelectorAll('[data-testid="cellInnerDiv"]');

	posts.forEach(async (post) => {
		const tweetArticle = post.querySelector('article[data-testid="tweet"]');
		if (!tweetArticle) return;

		const postId = Array.from(tweetArticle.querySelectorAll("a"))
			.find((a) => a.href.includes("/status/"))
			?.href.split("/")
			.find((part, index, array) => array[index - 1] === "status");
		const postTextElement = tweetArticle.querySelector(
			'[data-testid="tweetText"]',
		);
		const postText = postTextElement ? postTextElement.innerText.trim() : "";

		// Check if the post is promoted
		const isPromoted =
			post.querySelector('[data-testid="promoted-icon"]') !== null;

		if (postId) {
			let analysis = await getCachedAnalysis(postId);
			if (!analysis) {
				analysis = await analyzeTweet(postText, apiKey);
				await cacheAnalysis(postId, analysis);
			}
			applyPostVisibility(
				postId,
				analysis,
				postText,
				initialKeywords,
				isPromoted,
			);
		}
	});
}

// Function to get cached analysis
async function getCachedAnalysis(postId) {
	return new Promise((resolve) => {
		chrome.storage.local.get([`analysis_${postId}`], (result) => {
			resolve(result[`analysis_${postId}`] || null);
		});
	});
}

// Function to cache analysis
async function cacheAnalysis(postId, analysis) {
	return new Promise((resolve) => {
		chrome.storage.local.set({ [`analysis_${postId}`]: analysis }, resolve);
	});
}

// Control variable to enable or disable the hiding feature
const enableHidingFeature = true; // Set to false to turn off the hiding feature

// Function to apply post visibility based on analysis and keywords
function applyPostVisibility(
	postId,
	analysis,
	postText,
	keywords,
	isPromoted,
) {
	if (typeof analysis === "object" && analysis !== null) {
		const shouldHideByTopic = topicsConfig.some(
			(topic) =>
				topic.topic in analysis && analysis[topic.topic] > topic.threshold,
		);

		const shouldHideByKeywords = keywords.some((word) =>
			postText.toLowerCase().includes(word.toLowerCase()),
		);

		const shouldHide =
			shouldHideByTopic ||
			shouldHideByKeywords ||
			isPromoted;

		const postElement = findPostElement(postId);
		if (postElement) {
			if (shouldHide && enableHidingFeature) {
				// Hide the post by creating a placeholder
				createPlaceholder(postElement, postId);
				logHiddenPost(
					postId,
					postElement,
					analysis,
					shouldHideByKeywords,
					isPromoted,
				);
			} else if (postElement.dataset.originalContent) {
				restoreOriginalContent(postElement, postId);
			} else {
				highlightKeywords(postElement);
			}
		} else {
			console.log(`Could not find element for post ${postId} to modify`);
		}
	} else {
		console.log(`Skipping post ${postId} due to invalid analysis result`);
	}
}

// Function to create a placeholder over the post
function createPlaceholder(postElement, postId) {
	// Check if the placeholder already exists
	let placeholder = postElement.querySelector('.hidden-post-placeholder');
	if (!placeholder) {
		// Create the overlay container
		placeholder = document.createElement('div');
		placeholder.className = 'hidden-post-placeholder';
		placeholder.style.cssText = `
			position: absolute;
			top: 0;
			left: 0;
			width: 100%;
			height: 100%;
			background-color: rgba(247, 249, 249, 0.97); /* Slightly transparent to hint at underlying content */
			display: flex;
			flex-direction: column;
			justify-content: center;
			align-items: center;
			z-index: 10;
			pointer-events: all;
		`;

		const message = document.createElement('div');
		message.style.cssText = `
			color: #657786;
			font-size: 15px;
			line-height: 20px;
			text-align: center;
			padding: 0 10px;
		`;
		message.textContent = 'This post has been hidden based on your filter settings.';
		placeholder.appendChild(message);

		const revealButton = document.createElement('button');
		revealButton.textContent = 'Reveal Post';
		revealButton.style.cssText = `
			background-color: #1DA1F2;
			color: #FFFFFF;
			border: none;
			border-radius: 9999px;
			padding: 8px 16px;
			margin-top: 12px;
			cursor: pointer;
			font-size: 14px;
			font-weight: bold;
			font-family: 'Segoe UI', Arial, sans-serif;
		`;
		revealButton.onmouseover = () => {
			revealButton.style.backgroundColor = '#0d95e8';
		};
		revealButton.onmouseout = () => {
			revealButton.style.backgroundColor = '#1DA1F2';
		};
		revealButton.onclick = () => restoreOriginalContent(postElement, postId);

		// Center the button
		const buttonContainer = document.createElement('div');
		buttonContainer.style.cssText = 'display: flex; justify-content: center;';
		buttonContainer.appendChild(revealButton);
		placeholder.appendChild(buttonContainer);

		// Ensure the post element is positioned relatively
		postElement.style.position = 'relative';

		// Append the placeholder overlay to the post element
		postElement.appendChild(placeholder);

		// Add the 'hidden' class to the post
		postElement.classList.add('post-hidden');
	} else {
		placeholder.style.display = 'flex';
	}
}

// Function to restore the original content of the post
function restoreOriginalContent(postElement, postId) {
	// Remove the placeholder overlay
	const placeholder = postElement.querySelector('.hidden-post-placeholder');
	if (placeholder) {
		placeholder.style.display = 'none';
	}

	// Remove the 'hidden' class from the post
	postElement.classList.remove('post-hidden');

	// Update the hidden posts log
	removeFromHiddenPostsLog(postId);
}

// Function to remove the post from the hidden posts log
function removeFromHiddenPostsLog(postId) {
	const index = hiddenPostsLog.findIndex(log => log.includes(`Post ${postId} hidden`));
	if (index !== -1) {
		hiddenPostsLog.splice(index, 1);
		updateOverlay();
	}
}

// Function to find the div element containing a specific post ID
function findPostElement(postId) {
	if (typeof postId !== "string") {
		throw new Error("postId must be a string");
	}
	const cellInnerDivs = document.querySelectorAll(
		'[data-testid="cellInnerDiv"]',
	);

	for (const div of cellInnerDivs) {
		const link = div.querySelector(`a[href*="/status/${postId}"]`);
		if (link) {
			return div;
		}
	}

	return null; // Return null if no matching element is found
}
window.findPostElement = findPostElement;

// Function to reset the cache (seenPostIds and analysis results)
function resetCache() {
	chrome.storage.local.get(null, (items) => {
		const allKeys = Object.keys(items);
		const analysisKeys = allKeys.filter((key) => key.startsWith("analysis_"));
		chrome.storage.local.remove(analysisKeys, () => {
			console.log("Cache (analysis results) has been reset.");
		});
	});
}

