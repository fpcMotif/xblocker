// Function to perform more advanced NLP-based filtering
function advancedChineseFilter(text) {
	// Convert traditional Chinese characters to simplified
	const simplifiedText = text.replace(/[\u2E80-\u2FD5\u3190-\u319f\u3400-\u4DBF\u4E00-\u9FCC\uF900-\uFAAD]/g, 
		char => traditionalToSimplified[char] || char);
	
	// Tokenize the text (simple word segmentation)
	const tokens = simplifiedText.match(/\b\w+\b|[\u4e00-\u9fa5]+/g) || [];
	
	// Check for keyword matches, including partial matches for Chinese characters
	return initialKeywords.some(keyword => 
		tokens.some(token => token.includes(keyword) || keyword.includes(token))
	);
}

