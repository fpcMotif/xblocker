// Add this new function to highlight keywords
function highlightKeywords(postElement) {
  const tweetTextElement = postElement.querySelector('[data-testid="tweetText"]');
  if (!tweetTextElement) return;

  const originalText = tweetTextElement.innerHTML;
  let highlightedText = originalText;

  keywordHighlights.forEach(({ category, color, keywords }) => {
    keywords.forEach(keyword => {
      const regex = new RegExp(`\\b${keyword}\\b`, 'gi');
      highlightedText = highlightedText.replace(regex, `<span style="background-color: ${color}; padding: 2px; border-radius: 3px;" title="${category}">${keyword}</span>`);
    });
  });

  if (highlightedText !== originalText) {
    tweetTextElement.innerHTML = highlightedText;
  }
}
